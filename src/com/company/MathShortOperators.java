package com.company;

public class MathShortOperators {
    public static void main(String[] args) {
        //psvm

        int num = 4;
        //num = num +5;
        num += 5;
        System.out.println(num);

        int num1 = 4;

        num1 -= 5;
        System.out.println(num1);

        int num2 = 4;

        num2 *= 5;
        System.out.println(num2);

        int num3 = 4;

        num3 /= 5;
        System.out.println(num3);



    }
}
